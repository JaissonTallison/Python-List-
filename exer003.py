# Escreva um programa que peça ao usuário dois números inteiros e exiba a soma, subtração, multiplicação e divisão desses números.

num1 = int(input("Digite um número: "))
num2 = int(input("Digite um segundo número: "))

soma = (num1 + num2)
subt = (num1 - num2)
mult = (num1 * num2)
div = (num1 / num2)

print(f"O resultado da soma: {soma}")
print(f"O resultado da subt: {subt}")
print(f"O resultado da mult: {mult}")
print(f"O resultado da div: {div}")
